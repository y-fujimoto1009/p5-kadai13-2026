let t = 0;

function setup() {
  createCanvas(746, 480);
  textFont("sans-serif");
}

function draw() {

 background(255);

  t++;

  //------------------------
  // 上の丸
  //------------------------

  noStroke();

  fill(130,28,33);
  circle(120, min(90, t), 60);

  fill(18,51,89);
  circle(185, min(145, max(0, t-30)), 60);

  fill(0,84,145);
  circle(120, min(165, max(0, t-50)), 35);

  //------------------------
  // 中央のマーク
  //------------------------

  strokeWeight(12);

  if(t>60){

    stroke(130,28,33);

    line(80,230,
         min(160,80+(t-60)*2),
         max(190,230-(t-60)));

    line(160,190,
         min(240,160+(t-60)*2),
         min(230,190+(t-60)));

    line(240,230,
         max(160,240-(t-60)*2),
         min(270,230+(t-60)));

    line(160,270,
         max(80,160-(t-60)*2),
         max(230,270-(t-60)));
  }


  //------------------------
  // SU
  //------------------------

  if(t>120){

    stroke(18,51,89);
    strokeWeight(12);

    line(80,250,155,287);
    line(80,250,155,350);
    line(155,350,80,313);
  }

  if(t>150){

    stroke(0,84,145);
    strokeWeight(12);

    line(170,350,170,287);
    line(170,350,240,313);
    line(240,313,240,250);
  }

//------------------------
  // 赤丸
  //------------------------

 if(t>150){
strokeWeight(3);
   stroke(255,255,255);

  fill(130,28,33);
  circle(175,215,min(30,t-150));

   strokeWeight(8);
   noStroke();
  
}

  


  //------------------------
  // キャッチコピー
  //------------------------

  if(t>210){

    fill(20,50,90);
    textSize(32);
    text("偉大なる平凡人たれ",310,90);
  }


  //------------------------
  // 100th
  //------------------------

  if(t>230){

    fill(130,28,33);

    textSize(120);
    text("100",min(330,150+t),220);

    textSize(60);
    text("th",min(540,360+t),220);
  }


  //------------------------
  // Anniversary
  //------------------------

  if(t>250){

    textSize(42);
    text("Anniversary",330,300);
  }


  //------------------------
  // Since
  //------------------------

  if(t>270){

    textSize(28);
    text("SINCE 1928",390,340);
  }
//------------------------
// 学校名
//------------------------

if(t>290){

  fill(130,28,33);
  textSize(28);
  textAlign(CENTER);

text("学校法人 大阪産業大学", 450,380);  

  textAlign(LEFT);
}


  //------------------------
  // お祝いアニメーション
  //------------------------

  if(t>210){

    // 紙吹雪
    noStroke();

    for(let i=0;i<40;i++){

      let x=(i*37+frameCount*3)%width;
      let y=(i*25+frameCount*5)%height;

      if(i%3==0){
        fill(130,28,33);
      }else if(i%3==1){
        fill(18,51,89);
      }else{
        fill(0,84,145);
      }

      rect(x,y,6,10);
    }

    // キラキラ
    stroke(255,215,0);
    strokeWeight(2);

    for(let i=0;i<8;i++){

      let x=430+cos(frameCount*0.05+i)*120;
      let y=170+sin(frameCount*0.05+i)*60;

      line(x-5,y,x+5,y);
      line(x,y-5,x,y+5);
    }

    noStroke();
  }
}
