let t = 0;

function setup() {
  createCanvas(746,480);
  textFont("sans-serif");
}

function draw() {

  background(255);

  t++;

  //------------------------
  // 上の丸
  //------------------------

  noStroke();

  fill(130,28,33);

  circle(max(120, 200 - t), max(90, 40 - t), 60);

  fill(18,51,89);

  circle(max(185, 250 - t), max(145, 90 - t), 60);

  fill(0,84,145);

  circle(max(120, 180 - t), max(165, 115 - t), 35);


  //------------------------
  // ロゴ本体
  //------------------------

  strokeWeight(12);

  if(t>30){

    stroke(130,28,33);

    line(max(80, 140 - (t-30)), 230,
         min(160, 80 + (t-30)*2),
         max(190, 230 - (t-30)));

    line(160, 190,
         min(240, 160 + (t-30)*2),
         min(230, 190 + (t-30)));

    line(240, 230,
         max(160, 240 - (t-30)*2),
         min(270, 230 + (t-30)));

    line(160, 270,
         max(80, 160 - (t-30)*2),
         max(230, 270 - (t-30)));
  }


  //------------------------
  // 青い部分
  //------------------------

  if(t>80){

    stroke(18,51,89);
    strokeWeight(12);

    line(max(80, 120 - (t-80)), 250, 155, 287);
    line(max(80, 120 - (t-80)), 250, 155, 350);
    line(155, 350, max(80, 120 - (t-80)), (350-37));
  }

  if(t>100){

    stroke(0,84,145);
    strokeWeight(12);

    line(170, 350, 170, 287);
    line(170, 350, min(240, 200 + (t-100)), (350-37));
    line(min(240, 200 + (t-100)), (350-37), min(240, 200 + (t-100)), 250);
  }

  //------------------------
  // 中央の赤丸
  //------------------------

  if(t>120){

    strokeWeight(3);
    stroke(255,255,255);

    fill(130,28,33);

    circle(175,215,min(30,t-120));

    strokeWeight(8);
    noStroke();
  }
  
  //------------------------
  // キャッチコピー
  //------------------------

  if(t>140){

    fill(20,50,90);

    textSize(32);

    text("偉大なる平凡人たれ", 310, max(90, 150 - (t-140)));

  }


  //------------------------
  // 100th
  //------------------------

  if(t>160){

    fill(130,28,33);

    textSize(120);

    text("100", 330, max(220, 280 - (t-160)));

    textSize(60);

    text("th", 540, max(220, 280 - (t-160)));

  }


  //------------------------
  // Anniversary
  //------------------------

  if(t>180){

    textSize(42);

    text("Anniversary", 330, max(300, 360 - (t-180)));

  }


  //------------------------
  // Since
  //------------------------

  if(t>200){

    textSize(28);

    text("SINCE 1928", 390, max(340, 400 - (t-200)));

  }


  //------------------------
  // 学校名
  //------------------------

  if(t>220){

    textSize(32);

    text("学校法人大阪産業大学", 300, max(400, 460 - (t-220)));

  }

}
