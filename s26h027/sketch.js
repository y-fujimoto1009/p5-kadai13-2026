let t = 0;

function setup() {
  createCanvas(746,480);
  textFont("sans-serif");
}

function draw() {

  background(255);
  t++;

  // 落下イージング
  function easeOutQuad(x){
    return 1 - (1 - x) * (1 - x);
  }

  // -----------------------------
  // ★ 最後のジャンプ（激しく1回）
  // -----------------------------
  let jumpOffset = 0;

  if(t > 600){
    let p = min(1, (t-600) / 60);   // 60フレームでジャンプ
    jumpOffset = sin(p * PI) * 150; // ← ジャンプの高さ（150px）
  }

  push();
  translate(0, -jumpOffset);   // ロゴ全体をジャンプさせる

  // -----------------------------
  // ① OSU の箱（赤い四角形）1.5倍速
  // -----------------------------
  strokeWeight(12);

  if(t>0){
    stroke(130,28,33);

    line(80,230, min(160,80+t*3), max(190,230-t*1.5));
    line(160,190, min(240,160+t*3), min(230,190+t*1.5));
    line(240,230, max(160,240-t*3), min(270,230+t*1.5));
    line(160,270, max(80,160-t*3), max(230,270-t*1.5));
  }

  // -----------------------------
  // ② 青い S（早めに表示）
  // -----------------------------
  if(t>80){
    stroke(18,51,89);
    strokeWeight(12);

    line(80,250,155,287);
    line(80,250,155,350);
    line(155,350,80,(350-37));
  }

  // -----------------------------
  // ③ 青い U（早めに表示）
  // -----------------------------
  if(t>140){
    stroke(0,84,145);
    strokeWeight(12);

    line(170,350,170,287);
    line(170,350,240,(350-37));
    line(240,(350-37),240,250);
  }

  // -----------------------------
  // ④ 丸（上から落ちる・2倍速）
  // -----------------------------
  noStroke();

  // 箱の中の赤丸
  if(t>240){
    let p = min(1, (t-240)/20);   // ← 2倍速
    let y = -100 + easeOutQuad(p) * (215 + 100);
    fill(255);
    circle(175,y,35);
    fill(130,28,33);
    circle(175, y, 30);
  }

  // 小さい青丸
  if(t>300){
    let p = min(1, (t-300)/20);   // ← 2倍速
    let y = -100 + easeOutQuad(p) * (165 + 100);
    fill(0,84,145);
    circle(120, y, 35);
  }

  // 青丸
  if(t>360){
    let p = min(1, (t-360)/20);   // ← 2倍速
    let y = -100 + easeOutQuad(p) * (145 + 100);
    fill(18,51,89);
    circle(185, y, 60);
  }

  // 赤丸
  if(t>420){
    let p = min(1, (t-420)/20);   // ← 2倍速
    let y = -100 + easeOutQuad(p) * (90 + 100);
    fill(130,28,33);
    circle(120, y, 60);
  }

  // -----------------------------
  // ⑤ 右側の文字（全部表示）
  // -----------------------------
  if(t>480){
    fill(20,50,90);
    textSize(32);
    text("偉大なる平凡人たれ",310,90);
  }

  if(t>500){
    fill(130,28,33);
    textSize(120);
    text("100",330,220);
    textSize(60);
    text("th",540,220);
  }

  if(t>520){
    textSize(42);
    text("Anniversary",330,300);
  }

  if(t>540){
    textSize(28);
    text("SINCE 1928",390,340);
  }

  if(t>560){
    textSize(32);
    text("学校法人大阪産業大学",300,400);
  }

  pop();  // ★ ジャンプ終了
}
