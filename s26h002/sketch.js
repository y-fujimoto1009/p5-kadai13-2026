let t = 0;

function setup() {
  createCanvas(746,480);
  textFont("sans-serif");
}

function draw() {

  background(255);

  t++;

  //------------------------
  // 「未来の自分」の文字
  //------------------------

  fill(130,28,33);
  noStroke();
  textSize(40);
  textAlign(CENTER, CENTER);

  let y1 = max(240, 420 - (t-30)*4);
  let y2 = max(190, 420 - (t-45)*4);
  let y3 = max(140, 420 - (t-60)*4);
  let y4 = max(90, 420 - (t-75)*4);
  let y5 = max(40, 420 - (t-90)*4);

  
  if(t>30){
    text("分",150,y3);
  }

 if(t>45){
    text("自",150,y4);
  }

  if(t>60){
    text("の",100,y3);
  }

  if(t>75){
    text("来",100,y4);
  }

  if(t>90){
    text("未",100,y5);
  }


  //------------------------
  // ロゴ本体
  //------------------------

  strokeWeight(12);

  if(t>60){

    stroke(130,28,33);

    line(80,230,
         min(160,80+(t-60)*2),
         max(190,230-(t-60)));

    line(160,190,
         min(240,160+(t-60)*2),
         min(230,190+(t-60)));

    line(240,230,
         max(160,240-(t-60)*2),
         min(270,230+(t-60)));

    line(160,270,
         max(80,160-(t-60)*2),
         max(230,270-(t-60)));
  }


  //------------------------
  // 青い部分
  //------------------------

  if(t>120){

    //"S"
    stroke(18,51,89);
    strokeWeight(12);

    line(80,250,155,287);
    line(80,250,155,350);
    line(155,350,80,(350-37));
  }

  if(t>150){

    //"U"
    stroke(0,84,145);
    strokeWeight(12);

    line(170,350,170,287);
    line(170,350,240,(350-37));
    line(240,(350-37),240,250);
  }

  //------------------------
  // 中央の赤丸
  //------------------------

  if(t>50){

    strokeWeight(3);
    stroke(255,255,255);

    fill(130,28,33);

    circle(175,215,min(30,t-150));

    strokeWeight(8);
    noStroke();
  }


  //------------------------
  // キャッチコピー
  //------------------------

  if(t>210){

    fill(20,50,90);

    textSize(32);

    text("偉大なる平凡人たれ",390,90);

  }


  //------------------------
  // 100th
  //------------------------

  if(t>230){

    fill(130,28,33);

    textSize(120);

    text("100",min(390,150+t),220);

    textSize(60);

    text("th",min(550,360+t),220);

  }


  //------------------------
  // Anniversary
  //------------------------

  if(t>250){

    textSize(42);

    text("Anniversary",390,300);

  }


  //------------------------
  // Since
  //------------------------

  if(t>270){

    textSize(28);

    text("SINCE 1928",390,340);

  }


  //------------------------
  // 学校名
  //------------------------

  if(t>290){

    textSize(32);

    text("学校法人大阪産業大学",300,400);

  }

}
