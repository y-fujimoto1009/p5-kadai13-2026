let t = 0;

function setup() {
  createCanvas(746, 480);
  textFont("sans-serif");
}

function draw() {

  // 少し明るい背景
  background(245, 250 + sin(frameCount * 0.02) * 3, 255);

  t++;

  //------------------------
  // 上の丸（拡大しながら出現）
  //------------------------

  noStroke();

  fill(235, 70, 90);
  let s1 = min(60, t * 0.8);
  circle(120, 90, s1);

  fill(70, 140, 255);
  let s2 = min(60, max(0, (t - 25)) * 0.8);
  circle(185, 145, s2);

  fill(80, 200, 255);
  let s3 = min(35, max(0, (t - 45)) * 0.5);
  circle(120, 165, s3);

  //------------------------
  // ロゴ本体（中心から伸びる）
  //------------------------

  if (t > 60) {

    let p = constrain((t - 60) / 40, 0, 1);

    stroke(235, 70, 90);
    strokeWeight(12);

    line(120, 230, lerp(80, 160, p), lerp(230, 190, p));
    line(160, 190, lerp(160, 240, p), lerp(190, 230, p));
    line(240, 230, lerp(240, 160, p), lerp(230, 270, p));
    line(160, 270, lerp(160, 80, p), lerp(270, 230, p));
  }

  //------------------------
  // 青い部分
  //------------------------

  if (t > 120) {

    stroke(70, 140, 255);
    strokeWeight(12);

    line(80, 250, 155, 287);
    line(80, 250, 155, 350);
    line(155, 350, 80, 313);
  }

  if (t > 150) {

    stroke(80, 200, 255);
    strokeWeight(12);

    line(170, 350, 170, 287);
    line(170, 350, 240, 313);
    line(240, 313, 240, 250);
  }

  //------------------------
  // 中央の丸
  //------------------------

  if (t > 150) {

    noStroke();
    fill(235, 70, 90);

    let r = min(30, (t - 150) * 1.5);
    circle(175, 215, r);
  }

  //------------------------
  // キャッチコピー（フェードイン）
  //------------------------

  if (t > 210) {

    let a = map(constrain(t, 210, 250), 210, 250, 0, 255);

    fill(40, 80, 180, a);
    textSize(32);
    text("偉大なる平凡人たれ", 310, 90);
  }

  //------------------------
  // 100th（ズームイン）
  //------------------------

  if (t > 230) {

    fill(235, 70, 90);

    let sz = min(120, (t - 230) * 4);

    textSize(sz);
    text("100", 330, 220);

    textSize(min(60, (t - 230) * 2));
    text("th", 560, 220);
  }

  //------------------------
  // Anniversary
  //------------------------

  if (t > 250) {

    fill(40, 80, 180);

    textSize(42);
    text("Anniversary", 330, 300);
  }

  //------------------------
  // Since
  //------------------------

  if (t > 270) {

    textSize(28);
    text("SINCE 1928", 390, 340);
  }

  //------------------------
  // 学校名
  //------------------------

  if (t > 290) {

    textSize(32);
    text("学校法人大阪産業大学", 300, 400);
  }

  //------------------------
  // 最後にキラキラ
  //------------------------

  if (t > 300) {

    noStroke();

    for (let i = 0; i < 10; i++) {

      fill(255, 220, 120, 180);

      circle(
        330 + random(250),
        120 + random(200),
        random(3, 7)
      );
    }
  }
}
