let t = 0;
function setup() {
  createCanvas(746,480);
  textFont("sans-serif");
}
function draw() {
  background(255);
  t++;
  //------------------------
  // ① 上の丸
  //------------------------
  if(t > 10){
    fill(130,28,33);
    circle(120,90, map(t,10,40,0,60,true));
  }
  if(t > 40){
    fill(18,51,89);
    circle(185,145, map(t,40,70,0,60,true));
  }
  if(t > 70){
    fill(0,84,145);
    circle(120,165, map(t,70,100,0,35,true));
  }
  //------------------------
  // ② ロゴ本体
  //------------------------
  strokeWeight(12);
  if(t > 100){
    stroke(130,28,33);
    let a = map(t,100,140,0,1,true);
    line(80,230, 80 + (160-80)*a, 190 + (190-230)*a);
    line(160,190, 160 + (240-160)*a, 190 + (230-190)*a);
    line(240,230, 240 + (160-240)*a, 230 + (270-230)*a);
    line(160,270, 160 + (80-160)*a, 270 + (230-270)*a);
  }
  if(t > 140){
    stroke(18,51,89);
    let b = map(t,140,170,0,1,true);
    line(80,250, 80 + (155-80)*b, 250 + (287-250)*b);
    line(80,250, 80 + (155-80)*b, 250 + (350-250)*b);
    line(155,350, 155 + (80-155)*b, 350 + (313-350)*b);
  }
  if(t > 170){
    stroke(0,84,145);
    let c = map(t,170,200,0,1,true);
    line(170,350, 170, 350 + (287-350)*c);
    line(170,350, 170 + (240-170)*c, 350 + (313-350)*c);
    line(240,313, 240, 313 + (250-313)*c);
  }
  //------------------------
  // ③ 中央の赤丸
  //------------------------
  if(t > 200){
    noStroke();
    fill(130,28,33);
    circle(175,215, map(t,200,230,0,30,true));
  }
  //------------------------
  // ④ キャッチコピー（細く）
  //------------------------
  if(t > 230){
    fill(20,50,90, map(t,230,260,0,255,true));
    textSize(34);
    stroke(20,50,90);
    strokeWeight(1.2);
    text("偉大なる平凡人たれ",300,95);
    noStroke();
  }
  //------------------------
  // ⑤ 100
  //------------------------
  if(t > 260){
    fill(130,28,33);
    textSize(130);
    let x = map(t,260,300,150,330,true);
    text("100",x,230);
  }
  //------------------------
  // ⑥ th
  //------------------------
  if(t > 300){
    textSize(62);
    stroke(130,28,33);
    strokeWeight(2.0);
    let x2 = map(t,300,340,700,545,true);
    text("th",x2,230);
    noStroke();
  }
  //------------------------
  // ⑦ Anniversary（赤色）
  //------------------------
  if(t > 340){
    fill(130,28,33, map(t,340,370,0,255,true)); // ★赤
    textSize(44);
    stroke(130,28,33);
    strokeWeight(2.3);
    text("Anniversary",330,305);
    noStroke();
  }
  //------------------------
  // ⑧ SINCE 1928（赤色）
  //------------------------
  if(t > 370){
    fill(130,28,33, map(t,370,400,0,255,true)); // ★赤
    textSize(30);
    stroke(130,28,33);
    strokeWeight(1.3);
    text("SINCE 1928",390,345);
    noStroke();
  }
  //------------------------
  // ⑨ 学校法人大阪産業大学（赤色）
  //------------------------
  if(t > 400){
    fill(130,28,33, map(t,400,430,0,255,true)); // ★赤
    textSize(34);
    stroke(130,28,33);
    strokeWeight(1.8);   // ★Anniversary/SINCE と同じ太さ
    text("学校法人大阪産業大学",300,405);
    noStroke();
  }
}
